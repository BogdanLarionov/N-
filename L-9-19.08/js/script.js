// найти в массиве преднаибольшее число
const arr = [4, 7, 12, 11];
let maxResalt = arr[0];
let predMaxResalt = arr[0];
for (let i = 0; i < arr.length; i++) {
  if (arr[i] > maxResalt) {
    predMaxResalt = maxResalt;
    maxResalt = arr[i];
  } else if (arr[i] > predMaxResalt) {
    predMaxResalt = arr[i];
  }
}
