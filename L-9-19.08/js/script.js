// написаить цикл (один), который находит сумму четных чисел и сумму нечетных.после цикла выведите эти значения
const arr = [12, 3, 32, 33, 5, 32, 34];
let sumEven = 0;
let sumOdd = 0;
for (let i = 0; i < arr.length; i++) {
  if (arr[i] % 2 === 0) {
    sumEven += arr[i];
  } else {
    sumOdd += arr[i];
  }
}
console.log(`Сумму четных чисел: ${sumEven}`); // Cумму четных чисел: 110
console.log(`Сумму нечетных чисел: ${sumOdd}`); // Cумму нечетных чисел: 41
